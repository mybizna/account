                                </div>
                            </div>
                            <div class="col-sm-4">
                                <label>{{
                                    this.$func.__("Billing Address", "erp")
                                }}</label>
                                <textarea
                                    v-model.trim="basic_fields.billing_address"
                                    rows="3"
                                    class="form-control form-contro-sm form-field"
                                    :placeholder="
                                        this.$func.__('Type here')
                                    "
                                ></textarea>
                            </div>

                            <check-fields
                                v-if="basic_fields.trn_by.id === '3'"
                                @updateCheckFields="setCheckFields"
                            />
                        </div>
                    </form>
                </div>
            </div>

            <div class="table-responsive">
                <!-- Start .crm-table -->
                <div class="table-container">
                    <table class="table form-table">
                        <thead>
                            <tr class="inline-edit-row">
                                <th scope="col" class="col--id">
                                    {{ this.$func.__("SL No.", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Account", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Description", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Amount", "erp") }}
                                </th>
                                <th scope="col">
                                    {{ this.$func.__("Total", "erp") }}
                                </th>
                                <th scope="col" class="col--actions"></th>
                            </tr>
                        </thead>
                        <tbody>
                            <tr
                                :key="key"
                                v-for="(line, key) in transactionLines"
                                class="inline-edit-row"
                            >
                                <td scope="row" class="col--id">
                                    {{ key + 1 }}
                                </td>
                                <td class="col--account with-multiselect">
                                    <multi-select
                                        v-model="line.ledger_id"
                                        :options="ledgers"
                                    />
                                </td>
                                <td class="col--particulars">
                                    <textarea
                                        v-model="line.particulars"
                                        rows="1"
                                        maxlength="250"
                                        class="form-control form-contro-sm form-field display-flex"
                                        :placeholder="
                                            this.$func.__('Particulars')
                                        "
                                    ></textarea>
                                </td>
                                <td
                                    class="col--amount"
                                    :data-colname="
                                        this.$func.__('Amount')
                                    "
                                >
                                    <input
                                        type="text"
                                        name="amount"
                                        autocomplete="off"
                                        v-model="line.amount"
                                        @keyup="updateFinalAmount"
                                        class="text-right form-control form-contro-sm form-field"
                                        :required="
                                            line.ledger_id ? true : false
                                        "
                                    />
                                </td>
                                <td
                                    class="col--total"
                                    :data-colname="
                                        this.$func.__('Total')
                                    "
                                >
                                    <input
                                        type="text"
                                        :value="moneyFormat(line.amount)"
                                        class="text-right form-control form-contro-sm form-field"
                                        readonly
                                        disabled
                                    />
                                </td>
                                <td
                                    class="delete-row"
                                    :data-colname="
                                        this.$func.__('Remove Above Selection')
                                    "
                                >
                                    <a @click.prevent="removeRow(key)" href="#"
                                        ><i class="flaticon-trash"></i
                                    ></a>
                                </td>
                            </tr>
                            <tr class="add-new-line inline-edit-row">
                                <td colspan="9" style="text-align: left">
                                    <button
                                        @click.prevent="addLine"
                                        class="btn btn-primary add-line-trigger"
                                    >
                                        <i class="flaticon-add-plus-button"></i
                                        >{{ this.$func.__("Add Line", "erp") }}
                                    </button>
                                </td>
                            </tr>

                            <tr class="total-amount-row inline-edit-row">
                                <td class="text-right pr-0" colspan="4">
                                    {{ this.$func.__("Total Amount", "erp") }}
                                </td>
                                <td
                                    class="text-right"
                                    :data-colname="
                                        this.$func.__('Total Amount')
                                    "
                                >
                                    <input
                                        type="text"
                                        class="text-right form-control form-contro-sm form-field"
                                        name="finalamount"
                                        :value="moneyFormat(finalTotalAmount)"
                                        readonly
                                        disabled
                                    />
                                </td>
                                <td class="text-right"></td>
                            </tr>

                            <tr class="form-group inline-edit-row">
                                <td colspan="9" style="text-align: left">
                                    <label>{{
                                        this.$func.__("Particulars", "erp")
                                    }}</label>
                                    <textarea
                                        v-model="particulars"
                                        rows="4"
                                        maxlength="250"
                                        class="form-control form-contro-sm form-field display-flex"
                                        :placeholder="
                                            this.$func.__('Internal Information')
                                        "
                                    ></textarea>
                                </td>
                            </tr>
                            <tr class="inline-edit-row">
                                <td>
                                    <div
                                        class="attachment-item"
                                        :key="index"
                                        v-for="(file, index) in attachments"
                                    >
                                        <img
                                            :src="
                                               mybizna_assets +
                                                '/images/file-thumb.png'
                                            "
                                        />
                                        <span
                                            class="remove-file"
                                            @click="removeFile(index)"
                                            >&#10007;</span
                                        >

                                        <div class="attachment-meta">
                                            <h3>{{ getFileName(file) }}</h3>
                                        </div>
                                    </div>
                                </td>
                            </tr>
                            <tr class="add-attachment-row inline-edit-row">
                                <td colspan="9" style="text-align: left">
                                    <div class="attachment-container">
                                        <label class="col--attachement">{{
                                            this.$func.__("Attachment", "erp")
                                        }}</label>
                                        <file-upload
                                            v-model="attachments"
                                            url="/invoices/attachments"
                                        />
                                    </div>
                                </td>
                            </tr>
                        </tbody>
                        <tfoot>
                            <tr class="inline-edit-row">
                                <td colspan="9" style="text-align: right">
                                    <combo-button
                                        v-if="draftToExpense()"
                                        :options="[
                                            {
                                                id: 'update',
                                                text: this.$func.__(
                                                    'Save Conversion',
                                                    'erp'
                                                ),
                                            },
                                        ]"
                                    />
                                    <combo-button
                                        v-else-if="editMode"
                                        :options="updateButtons"
                                    />
                                    <combo-button
                                        v-else
                                        :options="createButtons"
                                    />
                                </td>
                            </tr>
                        </tfoot>
                    </table>
                </div>
            </div>
        </form>
    </div>
</template>

<script>

export default {
    components: {
        SelectAccounts: window.$func.fetchComponent('components/select/SelectAccounts.vue'),
        Datepicker: window.$func.fetchComponent('components/base/Datepicker.vue'),
        MultiSelect: window.$func.fetchComponent('components/select/MultiSelect.vue'),
        FileUpload: window.$func.fetchComponent('components/base/FileUpload.vue'),
        ComboButton: window.$func.fetchComponent('components/select/ComboButton.vue'),
        SelectPeople: window.$func.fetchComponent('partner/widgets/SelectPeople.vue'),
        CheckFields: window.$func.fetchComponent('components/check/CheckFields.vue'),
        ShowErrors: window.$func.fetchComponent('components/base/ShowErrors.vue'),
    },

    data() {
        return {
            basic_fields: {
                people: "",
                trn_ref: "",
                trn_date: "",
                deposit_to: "",
                trn_by: "",
                billing_address: "",
            },

            check_data: {
                bank_name: "",
                payer_name: "",
                check_no: "",
            },

            bank_data: {
                trn_charge: 0,
            },

            form_errors: [],

            createButtons: [
                { id: "save", text: this.$func.__("Save", "erp") },
                // {id: 'send_create', text: this.$func.__('Create and Send') },
                { id: "new_create", text: this.$func.__("Save and New", "erp") },
                { id: "draft", text: this.$func.__("Save as Draft", "erp") },
            ],

            updateButtons: [
                { id: "update", text: this.$func.__("Update", "erp") },
                // {id: 'send_update', text: this.$func.__('Update and Send')},
                { id: "new_update", text: this.$func.__("Update and New", "erp") },
                { id: "draft", text: "Save as Draft" },
            ],

            editMode: false,
            voucherNo: 0,
            transactionLines: [],
            selected: [],
            ledgers: [],
            pay_methods: [],
            attachments: [],
            totalAmounts: 0,
            finalTotalAmount: 0,
            particulars: "",
            isWorking: false,
            accts_by_chart: [],
           mybizna_assets: this.$mybizna_var.acct_assets /* global this.$mybizna_var */,
        };
    },

    watch: {
        finalTotalAmount(newval) {
            this.finalTotalAmount = newval;
        },

        "basic_fields.people"() {
            this.getPeopleAddress();
        },

        "basic_fields.trn_by"() {
            this.changeAccounts();
        },
    },

    computed: {
        ...mapState({ actionType: (state) => state.combo.btnID }),
    },
    emits: {

        // Validate submit event
        "remove-row": ({ index }) => {
             this.$delete(this.transactionLines, index);
            this.updateFinalAmount();
            return true;
        },

         "combo-btn-select": ({ button }) => {
             this.actionType = button.id;
            return true;
        }
    },
    created() {
        this.prepareDataLoad();
    },

    methods: {
        async prepareDataLoad() {
            /**
             * ----------------------------------------------
             * check if editing
             * -----------------------------------------------
             */
            if (this.$route.params.id) {
                this.editMode = true;
                this.voucherNo = this.$route.params.id;

                /**
                 * Duplicates of
                 *? this.getLedgers()
                 *? this.getPayMethods()
                 */
                const expense_chart_id = 5;

                const [request1, request2] = await Promise.all([
                    window.axios.get(`/ledgers/${expense_chart_id}/accounts`),
                    window.axios.get("/transactions/payment-methods"),
                ]);

                const request3 = await window.axios.get(
                    `/expenses/${this.$route.params.id}`
                );

                this.ledgers = request1.data;
                this.pay_methods = request2.data;
                this.setDataForEdit(request3.data);

                // initialize combo button id with `update`
                this.$store.dispatch("combo/setBtnID", "update");
            } else {
                /**
                 * ----------------------------------------------
                 * create a new expense
                 * -----------------------------------------------
                 */
                this.getLedgers();
                this.getPayMethods();

                this.basic_fields.trn_date = this.$mybizna_var.current_date;
                this.basic_fields.due_date = this.$mybizna_var.current_date;
                this.transactionLines.push({}, {}, {});

                // initialize combo button id with `save`
                this.$store.dispatch("combo/setBtnID", "save");
            }
        },

        setDataForEdit(expense) {
            this.basic_fields.people = {
                id: parseInt(expense.people_id),
                name: expense.people_name,
            };
            this.basic_fields.deposit_to = { id: parseInt(expense.deposit_to) };
            this.basic_fields.trn_by = this.pay_methods.find(
                (method) => method.id === expense.trn_by
            );
            this.basic_fields.billing_address = expense.address;
            this.basic_fields.trn_date = expense.trn_date;
            this.basic_fields.trn_ref = expense.ref;
            this.status = expense.status;
            this.particulars = expense.particulars;
            this.attachments = expense.attachments;

            // format transaction lines
            expense.bill_details.forEach((detail) => {
                this.transactionLines.push({
                    id: detail.id,
                    ledger_id: {
                        id: detail.ledger_id,
                        name: detail.ledger_name,
                    },
                    particulars: detail.particulars,
                    amount: detail.amount,
                });
            });

            this.updateFinalAmount();
        },

        draftToExpense() {
            return this.$route.query.convert;
        },

        getLedgers() {
            const expense_chart_id = 5;

            window.axios
                .get(`/ledgers/${expense_chart_id}/accounts`)
                .then((response) => {
                    this.ledgers = response.data;
                });
        },

        getPayMethods() {
            window.axios
                .get("/transactions/payment-methods")
                .then((response) => {
                    this.pay_methods = response.data;
                })
                .catch((error) => {
                    throw error;
                });
        },

        setCheckFields(check_data) {
            this.check_data = check_data;
        },

        getPeopleAddress() {
            const user_id = this.basic_fields.people.id;

            if (!user_id) {
                this.basic_fields.billing_address = "";
                return;
            }

            window.axios.get(`/people/${user_id}`).then((response) => {
                const billing = response.data;

                if (typeof billing === "string") {
                    this.basic_fields.billing_address = billing;
                    return;
                }

                let street_1 = billing.street_1 ? billing.street_1 + "," : "";
                let street_2 = billing.street_2 ? billing.street_2 : "";
                let city = billing.city ? billing.city : "";
                let state = billing.state ? billing.state + "," : "";
                let postal_code = billing.postal_code
                    ? billing.postal_code
                    : "";
                let country = billing.country ? billing.country : "";

                const address = `${street_1} ${street_2} \n${city} \n${state} ${postal_code} \n${country}`;

                this.basic_fields.billing_address = address;
            });
        },

        updateFinalAmount() {
            let finalAmount = 0;

            this.transactionLines.forEach((element) => {
                if (element.amount) {
                    finalAmount += parseFloat(element.amount);
                }
            });

            this.finalTotalAmount = parseFloat(finalAmount).toFixed(2);
        },

        addLine() {
            this.transactionLines.push({});
        },

        updateExpense(requestData) {
            window.axios
                .put(`/expenses/${this.voucherNo}`, requestData)
                .then((res) => {

                    var message = this.$func.__("Expense Updated!", "erp");

                    if (this.draftToExpense()) {
                        message = this.$func.__("Conversion Successful!", "erp");
                    }

                    this.showAlert("success", message);
                })
                .catch((error) => {
                    throw error;
                })
                .then(() => {
                    this.isWorking = false;
                    this.reset = true;

                    if (
                        this.actionType === "update" ||
                        this.actionType === "draft"
                    ) {
                        this.$router.push({ name: "Expenses" });
                    } else if (this.actionType === "new_update") {
                        this.resetFields();
                    }
                });
        },

        createExpense(requestData) {
            window.axios
                .post("/expenses", requestData)
                .then((res) => {
                    this.showAlert("success", this.$func.__("Expense Created!", "erp"));
                    this.$router.push({ name: "Expenses" });
                })
                .catch((error) => {
                    throw error;
                })
                .then(() => {
                    // this.isWorking = false;
                    // this.reset = true;
                    /* if (this.actionType === 'save' || this.actionType === 'draft') {
                    this.$router.push({ name: 'Expenses' });
                } else if (this.actionType === 'new_create') {
                    this.resetFields();
                }*/
                });
        },

        SubmitForExpense() {
            this.validateForm();

            if (this.form_errors.length) {
                window.scrollTo({
                    top: 10,
                    behavior: "smooth",
                });
                return;
            }

            this.isWorking = true;

            let trn_status = null;
            if (this.actionType === "draft") {
                trn_status = 1;
            } else {
                trn_status = 4;
            }

            let deposit_id = this.basic_fields.deposit_to.id;

            if (
                Object.prototype.hasOwnProperty.call(
                    this.basic_fields.deposit_to,
                    "people_id"
                )
            ) {
                deposit_id = this.basic_fields.deposit_to.people_id;
            }

            let bank_trn_charge = 0;

            if (parseInt(this.basic_fields.trn_by.id) === 2) {
                bank_trn_charge = this.bank_data.trn_charge || 0;
            }

            const requestData = {
                people_id: this.basic_fields.people.id,
                ref: this.basic_fields.trn_ref,
                trn_date: this.basic_fields.trn_date,
                trn_by: this.basic_fields.trn_by.id,
                bill_details: this.formatTrnLines(this.transactionLines),
                deposit_to: deposit_id,
                billing_address: this.basic_fields.billing_address,
                attachments: this.attachments,
                type: "expense",
                status: trn_status,
                particulars: this.particulars,
                check_no: parseInt(this.check_data.check_no),
                name: this.check_data.payer_name,
                bank: this.check_data.bank_name,
                convert: this.$route.query.convert,
                bank_trn_charge: bank_trn_charge,
            };

            if (this.editMode) {
                this.updateExpense(requestData);
            } else {
                this.createExpense(requestData);
            }
        },

        changeAccounts() {
            this.accts_by_chart = [];
            if (
                this.basic_fields.trn_by.id === "2" ||
                this.basic_fields.trn_by.id === "3"
            ) {
                window.axios.get("/ledgers/bank-accounts").then((response) => {
                    this.accts_by_chart = response.data;
                    this.accts_by_chart.forEach((element) => {
                        if (
                            !Object.prototype.hasOwnProperty.call(
                                element,
                                "balance"
                            )
                        ) {
                            element.balance = 0;
                        }
                    });
                });
            } else if (this.basic_fields.trn_by.id === "1") {
                window.axios.get("/ledgers/cash-accounts").then((response) => {
                    this.accts_by_chart = response.data;
                    this.accts_by_chart.forEach((element) => {
                        if (
                            !Object.prototype.hasOwnProperty.call(
                                element,
                                "balance"
                            )
                        ) {
                            element.balance = 0;
                        }
                    });
                });
                /* globalmybizna_reimbursement_var */
            } else if (this.basic_fields.trn_by.id === "4") {
                if (
                   mybizna_reimbursement_var.reimbursement_module !==
                        "undefined" &&
                   mybizna_reimbursement_var.reimbursement_module === "1"
                ) {
                    window.axios
                        .get("/people-transactions/balances")
                        .then((response) => {
                            this.accts_by_chart = response.data;
                            this.accts_by_chart.forEach((element) => {
                                if (
                                    !Object.prototype.hasOwnProperty.call(
                                        element,
                                        "balance"
                                    )
                                ) {
                                    element.balance = 0;
                                }
                            });
                        });
                }
            }
            this.$root.$emit("account-changed");
        },

        resetFields() {
            this.basic_fields = {
                people: "",
                trn_ref: "",
                trn_date: this.$mybizna_var.current_date,
                deposit_to: "",
                trn_by: "",
                billing_address: "",
            };

            this.check_data = {
                bank_name: "",
                payer_name: "",
                check_no: "",
            };

            this.form_errors = [];
            this.transactionLines = [];
            this.attachments = [];
            this.totalAmounts = 0;
            this.finalTotalAmount = 0;
            this.particulars = "";
            this.isWorking = false;

            this.transactionLines.push({}, {}, {});

            this.$store.dispatch("combo/setBtnID", "save");
        },

        validateForm() {
            this.form_errors = [];

            if (
                !Object.prototype.hasOwnProperty.call(
                    this.basic_fields.people,
                    "id"
                )
            ) {
                this.form_errors.push(this.$func.__("People Name is required.", "erp"));
            }

            if (!this.basic_fields.trn_date) {
                this.form_errors.push(
                    this.$func.__("Transaction Date is required.", "erp")
                );
            }

            if (
                !Object.prototype.hasOwnProperty.call(
                    this.basic_fields.deposit_to,
                    "id"
                )
            ) {
                this.form_errors.push(
                    this.$func.__("Transaction Account is required.", "erp")
                );
            }

            if (
                !Object.prototype.hasOwnProperty.call(
                    this.basic_fields.trn_by,
                    "id"
                )
            ) {
                this.form_errors.push(this.$func.__("Payment Method is required.", "erp"));
            }

            if (
                parseFloat(this.basic_fields.deposit_to.balance) <
                parseFloat(this.finalTotalAmount)
            ) {
                this.form_errors.push(
                    this.$func.__("Not enough balance in selected account.", "erp")
                );
            }

            if (!parseFloat(this.finalTotalAmount)) {
                this.form_errors.push(this.$func.__("Total amount can't be zero.", "erp"));
            }

            if (this.noFulfillLines(this.transactionLines, "ledger_id")) {
                this.form_errors.push(this.$func.__("Please select an account.", "erp"));
            }
        },

        removeRow(index) {
            this.$delete(this.transactionLines, index);
            this.updateFinalAmount();
        },

        formatTrnLines(trn_lines) {
            const line_items = [];

            trn_lines.forEach((element) => {
                if (
                    Object.prototype.hasOwnProperty.call(element, "ledger_id")
                ) {
                    element.ledger_id = element.ledger_id.id;
                    line_items.push(element);
                }
            });

            return line_items;
        },
    },
