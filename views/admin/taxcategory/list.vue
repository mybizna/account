<template>
    <div class="app-tax-categories">
        <div class="content-header-section separator">
            <div class="wperp-row wperp-between-xs">
                <div class="wperp-col">
                    <h2 class="content-header__title">
                        {{ __("Tax Categories", "erp") }}
                    </h2>
                    <a
                        class="wperp-btn btn--primary"
                        @click.prevent="showModal = true"
                    >
                        <span>{{ __("Add Tax Category", "erp") }}</span>
                    </a>
                </div>
            </div>
        </div>

        <new-tax-category
            v-if="showModal"
            :cat_id="cat_id"
            :is_update="is_update"
            @close="showModal = false"
        ></new-tax-category>

        <div class="wperp-row">
            <div class="table-container wperp-col-sm-8">
                <list-table
                    tableClass="wp-ListTable widefat fixed tax-rate-list wperp-table table-striped table-dark tax-cats-list"
                    action-column="actions"
                    :columns="columns"
                    :rows="row_data"
                    :total-items="paginationData.totalItems"
                    :total-pages="paginationData.totalPages"
                    :per-page="paginationData.perPage"
                    :current-page="paginationData.currentPage"
                    @pagination="goToPage"
                    :actions="actions"
                    :bulk-actions="bulkActions"
                    @action:click="onActionClick"
                    @bulk:click="onBulkAction"
                >
                </list-table>
            </div>
            <div class="wperp-col-sm-4">
                <tax-shortcuts></tax-shortcuts>
            </div>
        </div>
    </div>
</template>

<script>
import ListTable from "admin/components/list-table/ListTable.vue";
import NewTaxCategory from "admin/components/tax/NewTaxCategory.vue";
import TaxShortcuts from "admin/components/tax/TaxShortcuts.vue";

export default {
    components: {
        ListTable,
        NewTaxCategory,
        TaxShortcuts,
    },

    data() {
        return {
            showModal: false,
            modalParams: null,
            columns: {
                tax_cat_name: {
                    label: __("Category Name", "erp"),
                    isColPrimary: true,
                },
                tax_cat_desc: { label: __("Description", "erp") },
                actions: { label: __("Actions", "erp") },
            },
            rows: [],
            paginationData: {
                totalItems: 0,
                totalPages: 0,
                perPage: 20,
                currentPage:
                    this.$route.params.page === undefined
                        ? 1
                        : parseInt(this.$route.params.page),
            },
            actions: [
                {
                    key: "edit",
                    label: __("Edit", "erp"),
                    iconClass: "flaticon-edit",
                },
                {
                    key: "trash",
                    label: __("Delete", "erp"),
                    iconClass: "flaticon-trash",
                },
            ],
            bulkActions: [
                {
                    key: "trash",
                    label: __("Trash", "erp"),
                    iconClass: "flaticon-trash",
                },
            ],
            tax_cats: [{}],
            buttonTitle: "",
            pageTitle: "",
            url: "",
            singleUrl: "",
            isActiveOptionDropdown: false,
            cat_id: null,
            is_update: false,
        };
    },

    created() {
        this.$root.$on("refetch_tax_data", () => {
            this.fetchItems();
            this.is_update = false;
        });

        this.$root.$on("modal_closed", () => {
            this.is_update = false;
        });

        this.fetchItems();
    },

    computed: {
        row_data() {
            const items = this.rows;
            items.map((item) => {
                item.tax_cat_id = item.id;
                item.tax_cat_name = item.name;
                item.tax_cat_desc = item.description;
            });
            return items;
        },
    },

    methods: {
        fetchItems() {
            this.rows = [];
            this.$store.dispatch("spinner/setSpinner", true);

            window.axios
                .get("tax-cats", {
                    params: {
                        per_page: this.paginationData.perPage,
                        page:
                            this.$route.params.page === undefined
                                ? this.paginationData.currentPage
                                : this.$route.params.page,
                    },
                })
                .then((response) => {
                    this.rows = response.data;
                    this.paginationData.totalItems = parseInt(
                        response.headers["x-wp-total"]
                    );
                    this.paginationData.totalPages = parseInt(
                        response.headers["x-wp-totalpages"]
                    );
                    this.$store.dispatch("spinner/setSpinner", false);
                })
                .catch((error) => {
                    this.$store.dispatch("spinner/setSpinner", false);
                    throw error;
                });
        },

        goToPage(page) {
            const queries = Object.assign({}, this.$route.query);
            this.paginationData.currentPage = page;
            this.$router.push({
                name: "PaginateTaxCategories",
                params: { page: page },
                query: queries,
            });

            this.$root.$on("refetch_tax_data", () => {
                this.fetchItems();
            });

            this.fetchItems();
        },

        singleTaxCategory(tax_id) {
            this.$router.push({
                name: "SingleTaxCategory",
                params: { id: tax_id },
            });
        },

        onActionClick(action, row, index) {
            switch (action) {
                case "trash":
                    if (confirm(__("Are you sure to delete?", "erp"))) {
                        this.$store.dispatch("spinner/setSpinner", true);
                        window.axios
                            .delete("tax-cats" + "/" + row.id)
                            .then((response) => {
                                this.$delete(this.rows, index);
                                this.$store.dispatch(
                                    "spinner/setSpinner",
                                    false
                                );
                                this.showAlert(
                                    "success",
                                    __("Deleted !", "erp")
                                );
                            })
                            .catch((error) => {
                                this.$store.dispatch(
                                    "spinner/setSpinner",
                                    false
                                );
                                throw error;
                            });
                    }
                    break;

                case "edit":
                    this.showModal = true;
                    this.cat_id = row.id;
                    this.is_update = true;
                    this.fetchItems();
                    break;

                default:
                    break;
            }
        },

        onBulkAction(action, items) {
            if (action === "trash") {
                if (confirm(__("Are you sure to delete?", "erp"))) {
                    this.$store.dispatch("spinner/setSpinner", true);

                    window.axios
                        .delete("tax-cats/delete/" + items.join(","))
                        .then((response) => {
                            const toggleCheckbox =
                                document.getElementsByClassName("column-cb")[0]
                                    .childNodes[0];

                            if (toggleCheckbox.checked) {
                                // simulate click event to remove checked state
                                toggleCheckbox.click();
                            }

                            this.fetchItems();
                            this.$store.dispatch("spinner/setSpinner", false);
                            this.showAlert("success", __("Deleted !", "erp"));
                        })
                        .catch((error) => {
                            this.$store.dispatch("spinner/setSpinner", false);
                            throw error;
                        });
                }
            }
        },
    },
};
</script>

